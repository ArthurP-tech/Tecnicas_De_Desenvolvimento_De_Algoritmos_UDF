# =====================================
# 2.1 Catálogo de Produtos
# =====================================

catalogo = [
    ("Smartphone", 1500, "Eletrônicos"),
    ("Tênis", 259, "Moda"),
    ("Notebook", 3500, "Eletrônicos"),
    ("Livro", 48, "Educação"),
    ("Fone de ouvido", 209, "Eletrônicos")
]

eletronicos = [produto for produto in catalogo if produto[2] == "Eletrônicos"]
assert len(eletronicos) == 3
print("Produtos eletrônicos:", eletronicos)

# =====================================
# 2.2 Carrinho de Compras
# =====================================

carrinho = {}
carrinho["Notebook"] = 1
carrinho["Fone de ouvido"] = 2
del carrinho["Notebook"]

assert "Notebook" not in carrinho
assert carrinho["Fone de ouvido"] == 2
print("Carrinho:", carrinho)

# =====================================
# 2.3 Cálculo de Total com Desconto
# =====================================

precos = [1500, 200, 3500]
precos_com_desconto = [p * 0.9 for p in precos]
total = sum(precos_com_desconto)

assert round(total, 2) == round(1500*0.9 + 200*0.9 + 3500*0.9, 2)
print(f"Total com desconto: R$ {total:.2f}")

# =====================================
# 2.4 Estoque por Categoria
# =====================================

estoque = {
    "Eletrônicos": ["Smartphone", "Notebook", "Fone de ouvido"],
    "Moda": ["Tênis", "Camisa"],
    "Educação": ["Livro"]
}

categoria_maior = max(estoque, key=lambda c: len(estoque[c]))
assert categoria_maior == "Eletrônicos"
print("Categoria com mais produtos:", categoria_maior)

# =====================================
# 3.1 Ranking de Jogadores
# =====================================

ranking = [("Ana", 120), ("Bruno", 280), ("Carlos", 180)]
ranking.sort(key=lambda x: x[1], reverse=True)

assert ranking[0][0] == "Bruno"
print("Top 3 jogadores:", ranking[:3])

# =====================================
# 3.2 Inventário de Itens
# =====================================

inventario = {
    "Espada": (1, "raro"),
    "Poção": (5, "comum"),
    "Armadura": (2, "épico")
}

raros = [item for item, (_, raridade) in inventario.items() if raridade in ["raro", "épico"]]
assert "Espada" in raros and "Armadura" in raros
print("Itens raros:", raros)

# =====================================
# 3.3 Histórico de Partidas
# =====================================

partidas = [
    ("Ana", "Bruno", "Ana"),
    ("Carlos", "Ana", "Carlos"),
    ("Bruno", "Carlos", "Carlos")
]

vitorias = {}
for _, _, vencedor in partidas:
    vitorias[vencedor] = vitorias.get(vencedor, 0) + 1

assert vitorias["Carlos"] == 2
print("Vitórias:", vitorias)

