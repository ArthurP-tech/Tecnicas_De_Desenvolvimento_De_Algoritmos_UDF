longas = [m["nome"] for m in playlist if m["duracao"] > 3]
assert "Música B" in longas and "Música C" in longas
print("Músicas com mais de 3 minutos:", longas)

# =====================================
# 4.2 Favoritar Músicas
# =====================================

favoritas = {
    "Música A": False,
    "Música B": True,
    "Música C": True
}

favoritas_listadas = [nome for nome, fav in favoritas.items() if fav]
assert len(favoritas_listadas) == 2
print("Favoritas:", favoritas_listadas)

# =====================================
# 4.3 Estatísticas da Playlist
# =====================================

musicas = [("Música A", 2.5), ("Música B", 3.2), ("Música C", 4.0)]
total = sum([dur for _, dur in musicas])
media = total / len(musicas)

assert round(media, 2) == round(total / 3, 2)
print(f"Total: {total:.2f} min | Média: {media:.2f} min")

